// Funzione che disegna un rettangolo sul canvas
function createRect(x, y, width, height, color) {
    ctx.fillStyle = color;                  // Imposta il colore di riempimento
    ctx.fillRect(x, y, width, height);      // Disegna il rettangolo
}

// Funzione che disegna i muri del labirinto 
function drawWalls() {
    for (let i = 0; i < map.length; i++) {          // Questo ciclo esterno scorre tutte le righe della mappa.
        for (let j = 0; j < map[0].length; j++) {   //Questo ciclo interno scorre tutte le colonne di una riga.
            if (map[i][j] == 1) {       // Se il valore è 1 == muro
                createRect(             // Disegna il blocco muro blu
                j * oneBlockSize,
                i * oneBlockSize,
                oneBlockSize,
                oneBlockSize,
                "blue"
                );
            }
        }
    }
}

// Funzione per disegnare i muri sul canvas
